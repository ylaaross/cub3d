#include "P_CUBE3D.h"

int	wall_check(char **map)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (map[i])
	{
		j = 0;
		while (map[i][j])
		{
			if (map[i][j] == '0')
			{
				if ((map[i][j - 1] && map[i][j - 1] == ' ') 
					|| (map[i][j + 1] && map[i][j + 1] == ' ') 
					|| (map[i - 1][j] && map[i - 1][j] == ' ') 
					|| (map[i + 1][j] && map[i + 1][j] == ' '))
				{
					return (0);
				}
			}
			j++;
		}
		i++;
	}
	return (1);
}
